    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Categoria cat = new Categoria();
        cat.setNome(txtCategoria.getText());
        
        CategoriaBean cb = new CategoriaBean(cat);
        
        if(cb.insert()){
            JOptionPane.showMessageDialog(null,"Registro inserido com sucesso");
            consultaTabela();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao inserir");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.valueOf(txtCodigo.getText());
        Categoria cat = new Categoria();
        cat.setId(codigo);
        cat.setNome(txtCategoria.getText());
        CategoriaBean cb = new CategoriaBean(cat);
        if(cb.update()){
            JOptionPane.showMessageDialog(null,"Registro alterado com sucesso");
            consultaTabela();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao alterar o registro");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.valueOf(txtCodigo.getText());
        Categoria cat = new Categoria();
        cat.setId(codigo);
        
        CategoriaBean cb = new CategoriaBean(cat);
        
        if(cb.delete()){
            JOptionPane.showMessageDialog(null,"Registro excluido com sucesso");
            consultaTabela();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao excluir o registro");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int codigo = Integer.valueOf(txtCodigo.getText());
        Categoria cat = new Categoria();
        cat.setId(codigo);
         CategoriaBean cb = new CategoriaBean(cat);
        if(cb.findById()){
            txtCategoria.setText(cat.getNome());
        }else{
            JOptionPane.showMessageDialog(null,"Nenhum registro encontrado");
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

